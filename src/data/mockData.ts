export interface Product {
  id: string; // virtual id from Mongo
  _id?: string; // optional if you still use it somewhere

  productNumber: string;
  name: string;
  brand: string;
  category: string;
  gender: string;

  description: string;
  fabrics: string;

  images: string[];

  purchasePrice: number;
  salesPrice: number;
  margin: number;
  useCustomMargin?: boolean;

  status: "active" | "inactive";

  variants: ProductVariant[];

  createdAt: string;
  updatedAt: string;
}

export interface ProductVariant {
  id?: string;
  size?: string;
  color?: string;
  colorCode?: string;
  sku?: string;
  price?: number;
}

export interface Customer {
  id: string;
  companyName: string;
  businessId: string;
  contactPerson: string;
  phone: string;
  email: string;
  address: string;
  city: string;
  postcode: string;
  totalSales: number;
  totalMargin: number;
  discountPercent: number;
  companyLogo?: string;
}

export interface QuoteItem {
  id: string;
  product: Product;
  quantity: number;
  unitPrice: number;
  discount: number;
  markingCost: number;
  internalMarkingCost?: number;
  showUnitPrice: boolean;
  showTotalPrice: boolean;
  hideMarkingCost: boolean;
  generateMockup: boolean;
  mockupImage?: string;
  imageUrl?: string;
}

export interface SpecialCost {
  name: string;
  amount: number;
}

export interface CustomerComment {
  comment?: string;
  timestamp: string;
}

export interface Quote {
  id: string;
  quoteNumber: string;
  customer: Customer;
  items: QuoteItem[];
  status: "draft" | "sent" | "approved" | "rejected" | "completed" | "expired";
  version: number;
  validUntil: string;
  additionalTerms: string;
  showTotalPrice: boolean;
  specialCosts?: SpecialCost[];
  createdAt: string;
  updatedAt: string;
  salesperson: string;
  totalAmount: number;
  totalMargin: number;
  customerComments?: CustomerComment[];
  customerResponse?: "pending" | "accepted" | "rejected" | "";
  respondedAt?: string;
  accessCode?: string;
}

export interface SalesOrder {
  id: string;
  orderNumber: string;
  quote: Quote;
  customer: Customer;
  items: QuoteItem[];
  totalAmount: number;
  totalMargin: number;
  salesperson: string;
  createdAt: string;
  status: "pending" | "processing" | "completed";
}

